%方程f(x)=x^4-8*x^3-6*x^2-16*x+4
x0=4;    %给定初始点x0,这里的x0不是迭代初始值
e=1.0e-10;  %控制误差
k=0;  %迭代初始值置零
x1=5;  %迭代初始值
a=[];  %迭代次数存储数组
b=[];  %每次迭代所得函数值存储数组
while abs(x1-x0)>e
    a=[a,k];
    b=[b,x1];
    x0=x1;  %迭代初始值为x0=5
    f1=4*x0^3-24*x0^2-12*x0-16;
    f2=12*x0^2-48*x0-12;
    x1=x0-f1/f2;
    k=k+1;
end
plot(a,b)  %作出迭代次数和目标函数值的关系曲线
x=x1
k=k-1
