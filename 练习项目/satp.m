function Te = satp(t,p,a,It)
%计算室外综合温度
%   需输入参数为时间t，墙面倾角p，表面太阳辐射吸收率a，表面总辐射强度It
twp=32;
twg=35.2;
b=[-0.35,-0.38,-0.42,-0.45,-0.47,-0.41,-0.28,-0.12,0.03,0.16,0.29,0.4,0.48,0.52,0.51,0.43,0.39,0.28,0.14,0,-0.1,-0.17,-0.23,-0.26];
tr=(twg-twp)/0.52;
T0=twp+b(t)*tr;
h=12.5+4.07*2+0.03*4;
Te=T0+a*It/h;
if p==0
    Te=Te-3.8;
end


end

